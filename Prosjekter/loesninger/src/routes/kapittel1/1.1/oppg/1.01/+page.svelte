<p>Statiske nettisider består typisk av et gitt antall web-komponenter og er lik for alle brukere. <br>
En statisk nettside kan fortsatt være interaktiv, men opplevelsen er typisk ikke skreddersydd. Dynamiske <br>
nettsider derimot er personlig og skreddersydde basert på forskjellige data om deg. Det kan f.eks være <br>
at en nettside ser ut på en måte for en bruker i Norge, men ser annerledes ut for en bruker i England. <br>
Dynamiske nettsider er typisk nettbutikker eller sosiale medier, mens statiske nettsider kan være porteføle-nettsider <br>
brosjyrelignende nettsider eller andre nettsider som har til hensikt å formidle informasjon. Når man lager <br>
nettsider bruker man typisk en blanding av HTML (Hypertext Markup Language), CSS (Cascading Style Sheets) og JavaScript.</p>

