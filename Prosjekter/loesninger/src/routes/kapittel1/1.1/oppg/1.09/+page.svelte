<h1>JavaScript</h1>
<p>Når vi lager <u>interaktive nettsider</u>, bruker vi programmeringsspråket
<b>JavaScript</b>. JavaScript-kode skrives i hovedsak mellom taggene <code>&lt;script&gt;</code>
og <code>&lt;/script&gt;</code>. Vi oppretter en tekstvariabel i script-elementet, slik:</p>
<p><code>let variabelNavn = "tekst"</code></p>

