<h1>Han har påført Facebook milliardbøter: – De vil være over loven</h1>
<h2 id="ingress">I Norge får Facebook-eier Meta en million i bot om dagen. Men dette er bare en liten dråpe i havet,<br>
mener mannen som først gikk til rettssak mot teknologigiganten.</h2>
<p>– Det norske Datatilsynet er en av de små aktørene som faktisk gjør noe, sier personvernsaktivist <u>Max Schrems</u> til NRK. <br>

    Nylig innførte Datatilsynet daglige bøter til <i>Meta</i>, som eier <i>Facebook</i> og <i>Instagram</i>, på én million kroner. <br>
    
    Den østerrikske juristen har blitt et kjent fjes i personvern- og IT-miljøet for sin årelange kamp mot overvåking. Derfor var han på plass under den årlige sikkeherhetsfestivalen i <b>Lillehammer</b>. <br>
    
    <u>Schrems</u> var den første som vant en europeisk rettssak mot <i>Facebook</i> og leder i dag rettighetsorganisasjonen NOYB. <br>
    
    Samlet har han påført Meta mer enn 18 milliarder norske kroner i bøter.</p>

